package patternpractice;

public class Pattern4 {
    public static void main(String[] args) {
        int N = 3;

        for (int i = 1; i <=N; i++) {
            for (int j = 1 ; j <= i ; j++) {

                System.out.print(i+ " ");
                
            }
            System.out.println();
        }
    }
    
}
