package patternpractice;

public class Pattern5 {
    public static void main(String[] args) {
        int N = 0;
        
        for(int i = 5; i>N; i--){

            for(int j = 0; j<i; j++){

                System.out.print(i+" ");
            }
            System.out.println();


        }
      
    }
}

